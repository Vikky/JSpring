package com.model;

public class Product {

	private String desc;
	private int price;
	
	public String getDesc(){
		return this.desc;
	}
	
	public int getPrice(){
		return this.price;
	}
	
	public void setDesc(String desc){
		this.desc=desc;
	}
	
	public void setPrice(int price){
		this.price=price;
	}
	
}
